/**
 * @file main.cpp
 * @brief Пример использования классов Stream и Device.
 *
 * В этой программе создаётся один поток, ему задаётся массовый расход,
 * и информация о нём выводится на экран.
 */

#include "Stream.hpp"
#include "Device.hpp"

/**
 * @brief Точка входа в программу.
 * @return 0 при успешном завершении.
 */
int main() {
    Stream s1(1);        ///< Создаём поток с именем "s1"
    s1.setMassFlow(10.5); ///< Устанавливаем массовый расход потока
    s1.print();            ///< Выводим информацию о потоке
    return 0;
}
